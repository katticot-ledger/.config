vim.api.nvim_create_autocmd('BufReadPost', {
  callback = function()
    local mark = vim.api.nvim_buf_get_mark(0, '"')
    local lnum = mark[1]
    if lnum > 0 and lnum <= vim.fn.line '$' then
      vim.cmd 'normal! g`"'
    end
  end,
})
